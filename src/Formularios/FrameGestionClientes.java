    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        try{
            
            int fila=0;
            int columna =0;
            String posicion="";
            //Jugador jugador;
            //Arbitro arbitro;
            Cliente cliente;
             
            fila=this.tablaClientes.getSelectedRow();
            
            posicion = (String)this.tablaClientes.getValueAt(fila,columna);
     
            cliente=Configuracion.listaClientes.obtenerPersonaPorPosicion(Integer.parseInt(posicion)-1);
      
           if(cliente != null){
                
                int confirmado = JOptionPane.showConfirmDialog(this,"Â¿seguro?");
                
                if(JOptionPane.OK_OPTION==confirmado){
                    
                     Configuracion.usuarios.eliminar(cliente.getNombre()); //eliminado usuario
                     Configuracion.listaClientes.eliminarCliente(cliente);  //eliminado cliente
                    

                    JOptionPane.showMessageDialog(this, "Eliminado");
                     
                }
                   
               
            }else{
                JOptionPane.showMessageDialog(this,"Cliente no encontrado");
            }

          
         this.cargarTabla();
            
        }catch(IndexOutOfBoundsException p){
            JOptionPane.showMessageDialog(this, "ElEMENTO NO SELECCIONADO: "+p);
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

