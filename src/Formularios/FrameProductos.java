     private boolean validarFormModificar(){
         boolean result = false;
        if(this.txtNombre.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL NOMBRE DEL CLIENTE", "NOMBRE", ERROR_MESSAGE);
        }else if(this.txtCategoria.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA LA CATEGORIA", "CATEGORIA", ERROR_MESSAGE);
        }else if(this.txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL ID", "ID", ERROR_MESSAGE);
        }else if(this.txtPrecio.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL PRECIO", "PRECIO", ERROR_MESSAGE);
        }else if(this.txtStock.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL STOCK", "STOCK", ERROR_MESSAGE);
        }else{
            if(this.validarModificacion()){
                
             result = true;   
             
            }
        }
        return result;     
    } 

 
     private boolean validarModificacion(){
         boolean result=true;
         
          if(!this.txtNombre.getText().equals(this.p.getNombre())){
            if(Configuracion.listaProductos.obtenerProductoPorNombre(this.txtNombre.getText())!=null){
              JOptionPane.showMessageDialog(this, "EL NOMBRE YA EXISTE", "NOMBRE",ERROR_MESSAGE);
              result=false;  
            }
            
          }else if(!this.txtID.getText().equals(p.getID())){
              if(Configuracion.listaProductos.obtenerProducto(txtID.getText())!=null){
                 JOptionPane.showMessageDialog(this, "ID YA REGISTRADO", "DNI", ERROR_MESSAGE);
                 result=false; 
              }
              
               
          }
         
         return result;
     } 
      
